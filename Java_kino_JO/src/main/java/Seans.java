public class Seans {

    String nazwa;

    String gatunek;

    String rok;

    String godzina;

    String sala;

    public Seans(String nazwa, String gatunek, String rok, String godzina, String sala) {
        this.nazwa = nazwa;
        this.gatunek = gatunek;
        this.rok = rok;
        this.godzina = godzina;
        this.sala = sala;
    }
}
